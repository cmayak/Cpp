#include <iostream>
using namespace std;

int main(){
    for(int i = 1; i < 11; i++){
        cout << i << endl; //counts up to 10
    }

    for(int j = 10; j = 0; j--){
        cout << j << endl; //counts down from 10
    }
    
    int x = 10;
    while(--x){ // counts numbers down
        cout << x << endl;
    }
}